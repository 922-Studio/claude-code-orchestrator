#!/usr/bin/env python3
"""Generate plans/INDEX.md — a status-at-a-glance index of every plan.

Works for BOTH plan formats:
  * .html plans  -> title from <h1>, status from the first `eyebrow` span
  * .md   plans  -> title from first `# `, status from YAML frontmatter
                    `status:` or a `**Status**:` line, goal from `**Goal**:`

Usage:  python3 scripts/build-plan-index.py [--check]
        --check  exit 1 if INDEX.md is stale (for pre-commit/CI), don't write.

The index is intentionally regenerated, never hand-edited. Active plans live in
plans/; completed ones in plans/archive/. Both dirs are scanned.
"""
from __future__ import annotations
import re, sys, html
from pathlib import Path

REPO = Path(__file__).resolve().parent.parent
PLANS = REPO / "plans"
INDEX = PLANS / "INDEX.md"
DATE_RE = re.compile(r"^(\d{4}-\d{2}-\d{2})-(.+?)\.(html|md)$")


def _clean(s: str) -> str:
    s = re.sub(r"<[^>]+>", "", s or "")        # strip tags
    s = html.unescape(s).replace("\r", "")
    return re.sub(r"\s+", " ", s).strip()


def parse(path: Path) -> dict:
    name = path.name
    m = DATE_RE.match(name)
    date = m.group(1) if m else ""
    text = path.read_text(encoding="utf-8", errors="replace")
    title = status = goal = ""

    if path.suffix == ".html":
        mt = re.search(r"<h1[^>]*>(.*?)</h1>", text, re.S | re.I)
        title = _clean(mt.group(1)) if mt else ""
        me = re.search(r'eyebrow[^>]*>(.*?)<', text, re.S | re.I)
        status = _clean(me.group(1)) if me else ""
    else:  # markdown
        mt = re.search(r"^#\s+(.+)$", text, re.M)
        title = _clean(mt.group(1)) if mt else ""
        # YAML frontmatter status: first, then **Status**: line
        fm = re.match(r"^---\n(.*?)\n---", text, re.S)
        if fm:
            ms = re.search(r"^status:\s*(.+)$", fm.group(1), re.M | re.I)
            if ms:
                status = _clean(ms.group(1))
        if not status:
            ms = re.search(r"^(?:- )?\*\*Status\*\*:\s*(.+)$", text, re.M)
            status = _clean(ms.group(1)) if ms else ""
        mg = re.search(r"^(?:- )?\*\*Goal\*\*:?\s*(.+)$", text, re.M)
        goal = _clean(mg.group(1))[:100] if mg else ""

    if not title:
        title = (m.group(2).replace("-", " ") if m else name)
    return {"file": name, "date": date, "title": title,
            "status": status or "—", "goal": goal, "fmt": path.suffix[1:]}


def collect(directory: Path) -> list[dict]:
    if not directory.is_dir():
        return []
    rows = []
    for p in directory.iterdir():
        if p.suffix in (".html", ".md") and not p.name.startswith("_") \
           and p.name != "INDEX.md":
            rows.append(parse(p))
    return sorted(rows, key=lambda r: (r["date"], r["file"]), reverse=True)


def render() -> str:
    active = collect(PLANS)
    archived = collect(PLANS / "archive")
    out = ["# 📋 Plans — Index",
           "",
           "> Auto-generated by `scripts/build-plan-index.py` — do not edit by hand.",
           f"> Active: **{len(active)}** · Archived: **{len(archived)}**",
           ""]

    def table(rows: list[dict]) -> list[str]:
        if not rows:
            return ["_none_", ""]
        t = ["| Date | Plan | Fmt | Status |",
             "|------|------|-----|--------|"]
        for r in rows:
            title = r["title"]
            if r["goal"]:
                title += f" — {r['goal']}"
            t.append(f"| {r['date'] or '—'} | `{r['file']}`<br>{title} "
                     f"| {r['fmt']} | {r['status']} |")
        t.append("")
        return t

    out += ["## Active", ""] + table(active)
    out += ["## Archive", ""] + table(archived)
    return "\n".join(out) + "\n"


def main() -> int:
    new = render()
    if "--check" in sys.argv:
        cur = INDEX.read_text(encoding="utf-8") if INDEX.exists() else ""
        if cur != new:
            print("plans/INDEX.md is stale — run: python3 scripts/build-plan-index.py")
            return 1
        print("plans/INDEX.md up to date")
        return 0
    INDEX.write_text(new, encoding="utf-8")
    print(f"wrote {INDEX.relative_to(REPO)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
